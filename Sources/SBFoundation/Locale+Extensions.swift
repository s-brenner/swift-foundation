extension Locale: ExpressibleByStringInterpolation, ExpressibleByStringLiteral {
    
    public init(stringLiteral value: String) {
        self.init(identifier: value)
    }
}

extension Locale {
    
    private func localizedDescription(in locale: Locale = .englishUnitedStates) -> String {
        locale
            .localizedString(forIdentifier: identifier)!
            .formatted(.camelcased)
            .removingAll(["(", ")", "[", "]", ",", ".", "-", "&", "’", "'"])
    }
    
    /// Array of available identifiers that contain the `_` character.
    ///
    /// This eliminates any generic identifiers that do not have a specified language.
    static var underscoredAvailableIdentifiers: [String] {
        availableIdentifiers
            .filter { $0.contains("_") }
            .sorted()
    }
    
    static var staticLocales: [String] {
        underscoredAvailableIdentifiers.map {
            let name = Locale(identifier: $0).localizedDescription()
            return "public static let \(name): Self = \"\($0)\""
        }
    }
    
    static var allCasesBuilder: [String] {
        underscoredAvailableIdentifiers.map {
            let name = Locale(identifier: $0).localizedDescription()
            return ".\(name),"
        }
    }
}
